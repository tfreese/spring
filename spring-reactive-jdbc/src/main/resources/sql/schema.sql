
create table if not exists DEPARTMENT
(
  DEPARTMENT_ID   BIGINT        NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 increment by 1) PRIMARY KEY,
  DEPARTMENT_NAME VARCHAR(100)  NOT NULL
);


create table if not exists EMPLOYEE (
  EMPLOYEE_ID         BIGINT        NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 increment by 1) PRIMARY KEY,
  EMPLOYEE_LASTNAME   VARCHAR (100) NOT NULL,
  EMPLOYEE_FIRSTNAME  VARCHAR (100) NOT NULL,
  DEPARTMENT_ID       BIGINT           NOT NULL,

  FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENT (DEPARTMENT_ID)
);
